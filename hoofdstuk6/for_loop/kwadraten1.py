getallen = [1, 2, 3]
print("De kwadraten van de getallen in", getallen, "zijn:")
# print het kwadraat van elk getal in de lijst
for getal in getallen:
    kwadraat = getal ** 2
    print("Kwadraat van", getal, "is", kwadraat)

