# genereer een lijst van getallen van 0 tot en met 3
# start op 0, einde op 4 (exclusief)
for getal in range(4):
    print(getal, end=" ")
print()

# genereer een lijst van getallen van 2 tot en met 4
# start op 2, einde op 5 (exclusief)
for getal in range(2, 5):
    print(getal, end=" ")
print()

# genereer een lijst van de eerste vijf even getallen
# start op 0, einde op 5*2 (exclusief), stapgrootte 2
for getal in range(0, 5*2, 2):
    print(getal, end=" ")
print()
